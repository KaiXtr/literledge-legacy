<h1> O Poeta Come Amendoim </h1>
Mastigado na gostosura quente de amendoim… <br />
Falado numa língua curumim <br />
De palavras incertas num remeleixo melado melancólico… <br />
Saem lentas frescas trituradas pelos meus dentes bons… <br />
Molham meus beiços que dão beijos alastrados <br />
E depois remurmuram sem malícia as rezas bem nascidas… <br />
Brasil amado não porque seja minha pátria, <br />
Pátria é acaso de migrações e do pão-nosso onde Deus der… <br />
Brasil que eu amo porque é o ritmo do meu braço aventuroso, <br />
O gosto dos meus descansos, <br />
O balanço das minhas cantigas amores e danças. <br />
Brasil que eu sou porque é a minha expressão muito engraçada, <br />
Porque é o meu sentimento pachorrento, <br />
Porque é o meu jeito de ganhar dinheiro, de comer e de dormir. <br />
<br />
<span> Mário de Andrade </span>
