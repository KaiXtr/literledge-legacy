<h1> Tapuia </h1>
As florestas ergueram braços peludos para esconder-te <br />
A tua carne triste se desabotoa nos seios <br />
recém-chegados do fundo das selvas. <br />
Pararam no teu olhar as noites do Amazonas <br />
mornas e imensas <br />
E no teu corpo longo. <br />
ficou dormindo a sombra das cinco estrelas do Cruzeiro. <br />
O mato acorda no teu sangue <br />
sonhos de tribos desaparecidas <br />
- filha de raças anônimas <br />
que se misturam em grandes adultérios! <br />
E erras sem rumo assim pelas beiras do rio <br />
que os teus antepassados te deixaram de herança <br />
O vento desarruma os teus cabelos soltos <br />
e modela o vestido na intimidade do teu corpo exato. <br />
À noite o rio te chama. <br />
Chamam-te vozes do fundo do mato. <br />
Então entregas à água <br />
demoradamente <br />
como uma flor selvagem <br />
ante a curiosidade das estrelas. <br />
<br />
<span> Raul Bopp </span>
