<h1> O cego e a guitarra </h1>
O ruído vário da rua <br />
Passa alto por mim que sigo. <br />
Vejo: cada coisa é sua <br />
Oiço: cada som é consigo. <br />
<br />
Sou como a praia a que invade <br />
Um mar que torna a descer. <br />
Ah, nisto tudo a verdade <br />
É só eu ter que morrer. <br />
<br />
Depois de eu cessar, o ruído. <br />
Não, não ajusto nada <br />
Ao meu conceito perdido <br />
Como uma flor na estrada. <br />
<br />
Cheguei à janela <br />
Porque ouvi cantar. <br />
É um cego e a guitarra <br />
Que estão a chorar. <br />
<br />
Ambos fazem pena, <br />
São uma coisa só <br />
Que anda pelo mundo <br />
A fazer ter dó. <br />
<br />
Eu também sou um cego <br />
Cantando na estrada, <br />
A estrada é maior <br />
E não peço nada. <br />
<br />
<span> Fernando Pessoa </span>
