<h1> Vencido está de amor </h1>
Vencido está de amor Meu pensamento <br />
O mais que pode ser Vencida a vida, <br />
Sujeita a vos servir e Instituída, <br />
Oferecendo tudo A vosso intento. <br />
<br />
Contente deste bem, Louva o momento <br />
Outra vez renovar Tão bem perdida; <br />
A causa que me guia A tal ferida, <br />
Ou hora em que se viu Seu perdimento. <br />
<br />
Mil vezes desejando Está segura <br />
Com essa pretensão Nesta empresa, <br />
Tão estranha, tão doce, Honrosa e alta <br />
<br />
Voltando só por vós Outra ventura, <br />
Jurando não seguir Rara firmeza, <br />
Sem ser no vosso amor Achado em falta. <br />
<br />
<span> Luís de Camões </span>
