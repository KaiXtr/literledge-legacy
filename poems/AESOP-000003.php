<h1> O Vento e o Sol </h1>
"O vento e o sol estavam disputando qual dos dois era o mais forte. <br />
De repente, viram um viajante que vinha caminhando. <br />
- Sei como decidir nosso caso. Aquele que coseguir fazer <br />
o viajante tirar o casaco, será o mais forte. Você começa, <br />
propôs o sol, retirando-se para trás de uma nuvem. <br />
O vento começou a soprar com toda a força. Quanto mais soprava, <br />
mais o homem ajustava o casaco ao corpo. Desesperado, então o vento retirou-se. <br />
O sol saiu de seu esconderijo e brilhou com todo o esplendor <br />
sobre o homem, que logo setiu calor e despiu o paletó." <br />
<br />
(O amor constroi, a violência arruína.) <br />
