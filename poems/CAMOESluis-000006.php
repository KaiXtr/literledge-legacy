<h1> A dor que a minha alma sente </h1>
A dor que a minha alma sente... <br />
Não a saiba toda a gente... <br />
Que estranho caso de amor... <br />
Que desejado tormento... <br />
<br />
Que venha a ser avarento, <br />
Das dores da minha dor! <br />
<br />
Por me não tratar pior, <br />
Se sabe ou se sente, não a digo a toda a gente! <br />
<br />
Minha dor e a causa dela. <br />
A ninguém ouso falar. <br />
<br />
Que seria aventurar, <br />
A perder-me ou perde-la, <br />
Pois só em padece-la a minha alma está contente. <br />
<br />
Viva no peito escondida... Dentro da alma sepultada... <br />
Ou me mate... Ou me dê vida... <br />
Ou viva eu triste ou contente, <br />
Não quero que saiba a gente! <br />
<br />
<span> Luís de Camões </span>
