<h1> Soneto </h1>
Tanta lágrima hei já, senhora minha, <br />
Derramado dos olhos sofredores, <br />
Que se foram com elas meus ardores <br />
E ânsia de amar que de teus dons me vinha. <br />
<br />
Todo o pranto chorei. Todo o que eu tinha, <br />
caiu-me ao peito cheio de esplendores, <br />
E em vez de aí formar terras melhores, <br />
Tornou minha alma sáfara e maninha. <br />
<br />
E foi tal o chorar por mim vertido, <br />
E tais as dores, tantas as tristezas <br />
Que me arrancou do peito vossa graça, <br />
<br />
Que de muito perder, tudo hei perdido! <br />
Não vejo mais surpresas nas surpresas <br />
E nem chorar sei mais, por mor desgraça! <br />
<br />
<span> Mário de Andrade </span>
