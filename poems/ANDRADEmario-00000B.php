<h1> Epitalâmio </h1>
O alto fulgor desta paixão insana <br />
Há-de cegar nossos corações <br />
E deserdados da esperança humana <br />
Palmilharemos por escuridões… <br />
<br />
Não mais te orgulharás da soberana <br />
Beleza! e eu, minhas cálidas canções <br />
Não mais dedilharei com mão ufana <br />
Na harpa de luz das minhas ilusões!… <br />
<br />
Pela realização que ora se ultima <br />
Vai apagar-se em breve o alto fulgor <br />
Que te inflama e ilumina o meu desejo… <br />
<br />
Como no último verso a última rima, <br />
Eu deporei, sem gozo e sem calor, <br />
Meu derradeiro beijo no teu beijo! <br />
<br />
<span> Mário de Andrade </span>
