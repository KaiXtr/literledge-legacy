<h1> Ode Marítima </h1>
Sozinho, no cais deserto, a esta manhã de Verão, <br />
Olho pró lado da barra, olho pró Indefinido, <br />
Olho e contenta-me ver, <br />
Pequeno, negro e claro, um paquete entrando. <br />
Vem muito longe, nítido, clássico à sua maneira. <br />
Deixa no ar distante atrás de si a orla vã do seu fumo. <br />
Vem entrando, e a manhã entra com ele, e no rio, <br />
Aqui, acolá, acorda a vida marítima, <br />
Erguem-se velas, avançam rebocadores, <br />
Surgem barcos pequenos detrás dos navios que estão no porto. <br />
Há uma vaga brisa. <br />
Mas a minh’alma está com o que vejo menos. <br />
Com o paquete que entra, <br />
Porque ele está com a Distância, com a Manhã, <br />
Com o sentido marítimo desta Hora, <br />
Com a doçura dolorosa que sobe em mim como uma náusea, <br />
Como um começar a enjoar, mas no espírito. <br />
<br />
Olho de longe o paquete, com uma grande independência de alma, <br />
E dentro de mim um volante começa a girar, lentamente. <br />
<br />
Os paquetes que entram de manhã na barra <br />
Trazem aos meus olhos consigo <br />
O mistério alegre e triste de quem chega e parte. <br />
Trazem memórias de cais afastados e doutros momentos <br />
Doutro modo da mesma humanidade noutros pontos. <br />
Todo o atracar, todo o largar de navio, <br />
É — sinto-o em mim como o meu sangue — <br />
Inconscientemente simbólico, terrivelmente <br />
Ameaçador de significações metafísicas <br />
Que perturbam em mim quem eu fui… <br />
<br />
Ah, todo o cais é uma saudade de pedra! <br />
E quando o navio larga do cais <br />
E se repara de repente que se abriu um espaço <br />
Entre o cais e o navio, <br />
Vem-me, não sei porquê, uma angústia recente, <br />
Uma névoa de sentimentos de tristeza <br />
Que brilha ao sol das minhas angústias relvadas <br />
Como a primeira janela onde a madrugada bate, <br />
E me envolve com uma recordação duma outra pessoa <br />
Que fosse misteriosamente minha. <br />
<br />
<span> Fernando Pessoa </span>
