<h1> As Meninas </h1>
Arabela <br />
abria a janela. <br />
<br />
Carolina <br />
erguia a cortina. <br />
<br />
E Maria <br />
olhava e sorria: <br />
“Bom dia!” <br />
<br />
Arabela <br />
foi sempre a mais bela. <br />
<br />
Carolina, <br />
a mais sábia menina. <br />
<br />
E Maria <br />
apenas sorria: <br />
“Bom dia!” <br />
<br />
Pensaremos em cada menina <br />
que vivia naquela janela; <br />
<br />
uma que se chamava Arabela, <br />
uma que se chamou Carolina. <br />
<br />
Mas a profunda saudade <br />
é Maria, Maria, Maria, <br />
<br />
que dizia com voz de amizade: <br />
“Bom dia!” <br />
<br />
