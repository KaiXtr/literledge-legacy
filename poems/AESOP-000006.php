<h1> A Moça e a Vasilha de Leite </h1>
"Uma moça ia ao mercado equilibrando, na cabeça, a vasilha do leite. <br />
No caminho, começou a calcular o lucro que teria com a venda dele. <br />
- Com este dinheiro, comprarei muito ovos. Naturalmente, <br />
nem todos estarão bons, mas, pelo menos, de três quartos deles sairão pintinhos. <br />
Levarei alguns para vender no mercado. Com o dinheiro que ganhar, <br />
aumentarei o estoque dos ovos. Tornarei a pô-los a chocar e, em breve, <br />
terei uma boa fazenda de criação. Ficando rica, os homens, pedir-me-ão em casamento. <br />
Escolherei, naturalmente, o mais forte, o mais rico e o mais bonito. <br />
Como me invejarão as amigas! Comprarei um lindo vestido de seda, para o casamento e, <br />
também, um bonito véu. Todos dirão que sou a noiva mais elegante da cidade. <br />
Assim pensando, sacudiu a cabeça, de contentamento. A vasilha do leite caiu ao chão, <br />
o leite esparramou-se pela estrada e nada sobrou para vender no mercado." <br />
<br />
(Não se deve contar com o ovo quando ele ainda está dentro da galinha.)
