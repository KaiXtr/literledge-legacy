<h1> Coco de Pagu </h1>
Pagu tem os olhos moles <br />
uns olhos de fazer doer. <br />
Bate-côco quando passa. <br />
Coração pega a bater. <br />
<br />
Eh Pagu eh! <br />
Dói porque é bom de fazer doer. <br />
<br />
Passa e me puxa com os olhos <br />
provocantissimamente. <br />
Mexe-mexe bamboleia <br />
pra mexer com toda a gente. <br />
<br />
Eli Pagu eh! <br />
Dói porque é bom de fazer doer. <br />
<br />
Toda a gente fica olhando <br />
o seu corpinho de vai-e-vem <br />
umbilical e molengo <br />
de não-sei-o-que-é-que-tem. <br />
<br />
Eh Pagu eh! <br />
Dói porque é bom de fazer doer. <br />
<br />
Quero porque te quero <br />
Nas formas do bem-querer. <br />
Querzinho de ficar junto <br />
que é bom de fazer doer. <br />
<br />
Eh Pagu eh! <br />
Dói porque é bom de fazer doer. <br />
