<h1> O Leão Apaixonado </h1>
Certa vez um leão se apaixonou pela filha de um lenhador e foi pedir <br />
a mão dela em casamento. O lenhador não ficou muito animado com a idéia de <br />
ver a filha com um marido perigoso daquele e disse ao leão que era uma honra, <br />
mas muito obrigado, não queria. O leão se irritou; sentindo o perigo, <br />
o homem foi esperto e fingiu concordava: <br />
- É uma honra, meu senhor. Mas que dentões o senhor tem! Que garras compridas! <br />
Qualquer moça ia ficar com medo. Se o senhor quer casar com minha filha, <br />
vai ter que arrancar os dentes e cortar as garras. <br />
O leão apaixonado foi correndo fazer o que o outro tinha mandado, depois <br />
voltou à casa do pai da moça e repetiu seu pedido de casamento. <br />
Mas o lenhador, que já não sentia medo daquele leão manso e desarmado, <br />
pegou um pau e tocou o leão para fora de casa. <br />
<br />
(Quem perde a cabeça por amor, sempre acaba mal.)
