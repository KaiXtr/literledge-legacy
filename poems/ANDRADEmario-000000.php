<h1> Aceitarás o amor como eu o encaro? </h1>
Aceitarás o amor como eu o encaro?… <br />
… Azul bem leve, um nimbo, suavemente <br />
Guarda-te a imagem, como um anteparo <br />
Contra estes móveis de banal presente. <br />
<br />
Tudo o que há de melhor e de mais raro <br />
Vive em teu corpo nu de adolescente, <br />
A perna assim jogada e o braço, o claro <br />
Olhar preso no meu, perdidamente. <br />
<br />
Não exijas mais nada. Não desejo <br />
Também mais nada, só te olhar, enquanto <br />
A realidade é simples, e isto apenas. <br />
<br />
Que grandeza… a evasão total do pejo <br />
Que nasce das imperfeições. O encanto <br />
Que nasce das adorações serenas. <br />
<br />
<span> Mário de Andrade </span>
