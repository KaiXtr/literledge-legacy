<h1> A Raposa e a Cegonha </h1>
A raposa e a cegonha mantinham boas relações e pareciam ser amigas sinceras. <br />
Certo dia, a raposa convidou a cegonha para jantar e, por brincadeira, <br />
botou na mesa apenas um prato raso contendo um pouco de sopa. <br />
Para ela, foi tudo muito fácil, mas a cegonha pode apenas molhar <br />
a ponta do bico e saiu dali com muita fome. <br />
<br />
- Sinto muito, disse a raposa, parece que você não gostou da sopa. <br />
- Não pense nisso, respondeu a cegonha. Espero que, em retribuição a esta visita, <br />
você venha em breve jantar comigo. <br />

No dia seguinte, a raposa foi pagar a visita. Quando sentaram à mesa, <br />
o que havia para o jantar estava contido num jarro alto, de pescoço <br />
comprido e boca estreita, no qual a raposa não podia introduzir o focinho. <br />
Tudo o que ela conseguiu foi lamber a parte externa do jarro. <br />
<br />
- Não pedirei desculpas pelo jantar, disse a cegonha, <br />
assim você sente no próprio estomago o que senti ontem. <br />
<br />
