<h1> Senhora minha, se de pura inveja </h1>
Senhora minha, se de pura inveja <br />
Amor me tolhe a vista delicada, <br />
A cor, de rosa e neve semeada, <br />
E dos olhos a luz que o Sol deseja, <br />
<br />
Não me pode tolher que vos não veja <br />
Nesta alma, que ele mesmo vos tem dada, <br />
Onde vos terei sempre debuxada, <br />
Por mais cruel inimigo que me seja. <br />
<br />
Nela vos vejo, e vejo que não nasce <br />
Em belo e fresco prado deleitoso <br />
Senão flor que dá cheiro a toda a serra. <br />
<br />
Os lírios tendes nu~a e noutra face. <br />
Ditoso quem vos vir, mas mais ditoso <br />
Quem os tiver, se há tanto bem na terra! <br />
<br />
<span> Luís de Camões </span>
