<h1> Tentação </h1>
Eu fechei os meus lábios para a vida <br />
E a ninguém beijo mais, meus lábios são, <br />
Cmo astros frios que, com a luz perdida, <br />
Rolam de caos em caos na escuridão. <br />
<br />
Não que a alma tenha já desiludida <br />
Ou me faleçam os desejos, não! <br />
O que outrem prejulgava uma descida, <br />
É subir para mim, elevação! <br />
<br />
Vejo o calvário por que anseio, vejo <br />
O Madeiro sublime, “Glórias” ouço, <br />
E subo! A terra geme… eu paro. (É um beijo.) <br />
<br />
A moita bole… Eu tremo. (É um corpo.) Oh Cruz, <br />
Como estás longe ainda! E eu sou tão moço! <br />
E em derredor de mim tudo seduz!… <br />
<br />
Écloga (imitado de Alberto de Oliveira) <br />
<br />
Tirsis, enquanto Melibeu procura <br />
Esgarrado caprídeo, sonolento <br />
Deita-se à sombra de pinhal e o vento <br />
Escuta, olhando os cirros pela altura. <br />
<br />
Chega porém das vargens Nise pura, <br />
Que o tem preso a seus pés, e ele, sedento <br />
De amor, mais o de sonhos lesto armento <br />
Guarda, que esse, de capros, na planura. <br />
<br />
Passa-lhe a ninfa ao lado. Ele então muda <br />
O olhar para essa frol de primavera, <br />
E diz, vendo-lhe os lábios e o regaço: <br />
<br />
– Ai se eu pudesse, em vez da à frauta ruda, <br />
Minha boca na tua, não tivera <br />
Então escuro o engenho, e o corpo lasso <br />
