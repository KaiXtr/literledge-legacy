					Escrito por Basílio da Gama em 1769, o Uraguai é um poema épico que conta a história da disputa entre jesuítas, índios e colonos nos Sete Povos das Missões, 
					no Rio Grande do Sul, sendo o motivo da expedição dos espanhóis e portugueses o intuito de descrever o conflito entre "civilizados europeus" e "indígenas 
					primatas" e a expulsão dos jesuítas para cumprir as cláusulas do Tratado de Madrid, de 1756. O épico é um marco da literatura brasileira ao romper com o 
					modelo padrão do poema épico e narrar um acontecimento muito recente (o livro foi escrito pouco menos de 20 anos depois dos eventos narrados no livro), além 
					de ser a maior representação do Arcadismo no Brasil. A obra possui diversas influências iluministas e reproduz uma visão caricata dos jesuítas e os culpa 
					pela colonização dos indígenas. <br />
