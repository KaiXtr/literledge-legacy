					<h1> Review </h1>
					Written by Basílio da Gama in 1769, Uraguai is an epic poem that tells the story of the dispute between Jesuits, Indians and settlers in the Seven Peoples 
					of the Missions, in Rio Grande do Sul, the reason for the expedition of the Spanish and Portuguese was to describe the conflict between "European 
					civilized" and "indigenous primates "and the expulsion of the Jesuits to comply with the provisions of the Treaty of Madrid, 1756. The epic is a milestone 
					in Brazilian literature when it breaks with the standard model of the epic poem and narrate a very recent event (the book was written just under 20 years 
					after the events narrated in the book), in addition to to be the largest representation of Arcadism in Brazil. The work has several Enlightenment 
					influences and reproduces a caricature vision of the Jesuits and blames them for the colonization of indigenous people. <br />
					<br />
					Uraguai is composed of only five corners and has 1377 white verses (without rhyme) and without stropulation, differentiating from the ten corners and 8816 
					verses decasyllables of <a href='http://localhost/literledge/books/00000S.php'> Os Lusíadas </a>. Furthermore, while Camões wrote his proem along with the
					invoking the Tagus nymphs, then his proposition and finally his dedication, Basílio chose to write the proem first, to add the invocation with the
					proposition and end, also, with the dedication. We see a similarity between the classic works in the speech from one character to the other, to explain the
					past events, as both start in media res. In the case of Os Lusíadas we have Vasco da Gama's speech to the king of Malindi. In O Uraguai we see
					similar situation through Andrade's speech to Catâneo. We also have in the work of Basílio a clear reference to that of Camões in Andrade's dialogue with
					Cacambo and Sepé (Corner II). Cacambo makes an emphatic argument against nature, saying that the sea had not fulfilled its role of separating the lands and 
					preventing Europeans arrived in America. Allusion to the discourse of Velho do Restelo (Canto IV, verse 102, of Os Lusíadas). The two epic poems bring as 
					heroes real characters. Gomes Freire de Andrade in O Uraguai and Vasco da Gama in Os Lusíadas.
